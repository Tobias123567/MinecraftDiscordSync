package eu.vironlab.minecraft.mds.bungee.server;

import net.md_5.bungee.api.ChatColor;
import net.md_5.bungee.api.ProxyServer;
import net.md_5.bungee.api.connection.ProxiedPlayer;

public class BungeeServerUtil {
	
	public static int getPlayerCount() {
		return ProxyServer.getInstance().getOnlineCount();
	}
	
	public static int getMaxPlayerCount() {
		return ProxyServer.getInstance().getOnlineCount();
	}
	
	public static String getPlayerList() {
		StringBuilder stringBuilder = new StringBuilder();
		for(ProxiedPlayer player : ProxyServer.getInstance().getPlayers()) {
			stringBuilder.append("+" + player.getName() + "\n");
		}
		return stringBuilder.length() == 0 ? "" : stringBuilder.toString();	
	}
	
	@SuppressWarnings("deprecation")
	public static boolean broadcastMessage(String message) {
		for(ProxiedPlayer player : ProxyServer.getInstance().getPlayers()) {
			player.sendMessage(ChatColor.translateAlternateColorCodes((char)'&', message));
		}
		return true;
	}

}
