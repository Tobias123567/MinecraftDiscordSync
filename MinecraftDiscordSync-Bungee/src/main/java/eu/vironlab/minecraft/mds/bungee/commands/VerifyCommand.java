package eu.vironlab.minecraft.mds.bungee.commands;

import java.util.UUID;

import eu.vironlab.minecraft.mds.bungee.BungeeMinecraftDiscordSync;
import eu.vironlab.minecraft.mds.verification.VerificationQueue;
import net.md_5.bungee.api.CommandSender;
import net.md_5.bungee.api.connection.ProxiedPlayer;
import net.md_5.bungee.api.plugin.Command;

public class VerifyCommand extends Command {

	public VerifyCommand() {
		super("verify");
	}
	
	@SuppressWarnings("deprecation")
	@Override
	public void execute(CommandSender sender, String[] args) {
		if(!sender.hasPermission("mcdcs.command.verify")) return;
		if(!(sender instanceof ProxiedPlayer)) return;
		String code = "";
		UUID uid = ((ProxiedPlayer)sender).getUniqueId();
		if(!VerificationQueue.verificationProcessing.containsKey(uid)) {
			code = VerificationQueue.generateRandom6Digit();
			VerificationQueue.verificationProcessing.put(uid, code);
		}
		String botPrefix = BungeeMinecraftDiscordSync.getInstance().getPluginAPI().getDiscordBot().getPrefix();
		sender.sendMessage(BungeeMinecraftDiscordSync.getInstance().getPluginMessages().translate("command.ingame.verify.success", botPrefix, "verify", code));
	}

}
